extension AuthBackground {
    /// To be able to adjust the positions and to output the parameters we use to the objects as a result
    /// - Parameters:
    ///   - plane: planeX will come there
    ///   - position: positionX will come there
    ///   - dw: This value is calculated as weight
    /// - Returns: It returns a double value, you can use it in positionX
    private func setBackgroundPositionW(plane: Bool ,position: Double, dw: Double) -> Double {
        (plane ? 1 * (position + ACYdw(aiachyState, d: dw)) : -1 * (position + ACYdw(aiachyState, d: dw)) )
    }
    /// To be able to adjust the positions and to output the parameters we use to the objects as a result
    /// - Parameters:
    ///   - plane: planeY will come there
    ///   - position: positionY will come there
    ///   - dh: This value is calculated as height
    /// - Returns: It returns a double value, you can use it in positionY
    private func setBackgroundPositionH(plane: Bool ,position: Double, dh: Double) -> Double {
        (plane ? 1 * (position + ACYdh(aiachyState, d: dh)) : -1 * (position + ACYdh(aiachyState, d: dh)) )
    }
    
    /// <#Description#>
    /// - Parameters:
    ///   - opaquedPlus: <#opaquedPlus description#>
    ///   - settedPlus: <#settedPlus description#>
    /// - Returns: <#description#>
    private func setOpaqued(opaquedPlus: Double, opaqued: Double) -> Double {
        guard !(opaquedPlus == 0.031) else { return opaquedPlus}
        guard !(opaquedPlus == 0.01 ) else { return opaquedPlus }
        
        let currentOpaqued = opaquedPlus + opaqued
        
        return setFilterOpaqued(currentOpaqued: currentOpaqued)
    }
    
    private func setFilterOpaqued(currentOpaqued: Double ) -> Double {
        guard currentOpaqued < 1 else { return currentOpaqued - 1 }
        guard currentOpaqued < 0.9 else { return currentOpaqued - 0.5 }
        guard currentOpaqued < 0.8 else { return currentOpaqued - 0.4 }
        guard currentOpaqued < 0.7 else { return currentOpaqued - 0.3 }
        guard currentOpaqued < 0.6 else { return currentOpaqued - 0.2 }
        guard currentOpaqued < 0.5 else { return currentOpaqued - 0.1 }
        guard currentOpaqued < 0.4 else { return currentOpaqued - 0.05}
        return currentOpaqued
    }
}

